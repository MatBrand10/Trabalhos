# Declaração e inicialização de variáveis
numero = 10          # Inteiro
preco = 5.99         # Float (real)
letra = 'A'          # Caractere (string de um caractere)
nome = "João"        # String
ativo = True         # Booleano

# Exibir valores
print(f"Número: {numero}")
print(f"Preço: {preco}")
print(f"Letra: {letra}")
print(f"Nome: {nome}")
print(f"Ativo: {ativo}")
